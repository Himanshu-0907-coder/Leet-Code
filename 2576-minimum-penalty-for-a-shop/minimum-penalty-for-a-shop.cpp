class Solution {
public:
    int bestClosingTime(string customers) {
        int n = customers.length();

        vector<int> pre(n+1), suf(n+1);   // FIXED
       
        pre[0] = 0;
        for(int i=0; i<n; i++){           // FIXED
            pre[i+1] = pre[i] + ((customers[i] == 'N') ? 1 : 0);
        }

        suf[n] = 0;
        for(int i=n-1; i>=0; i--){
            suf[i] = suf[i+1] + ((customers[i] == 'Y') ? 1 : 0);
        }

        int minPen = n;
        for(int i=0; i<=n; i++){
            pre[i] += suf[i];
            minPen = min(minPen, pre[i]);
        }
        
        for(int i=0; i<=n; i++){
            if(pre[i] == minPen) return i;   // FIXED
        }
        return n;
    }
};
